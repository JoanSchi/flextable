import 'package:flextable/flextable.dart';

import 'flextable_change_notifier.dart';

class FlexTableScaleChangeNotifier
    extends FlexTableChangeNotifier<FlexTableScaleNotification> {
  FlexTableScaleChangeNotifier();

  notify(Function(FlexTableScaleNotification listener) call) {
    notifyListeners(call);
  }
}

abstract class FlexTableScaleNotification {
  scaleChange(
    FlexTableModel flexTableModel,
  );
}
